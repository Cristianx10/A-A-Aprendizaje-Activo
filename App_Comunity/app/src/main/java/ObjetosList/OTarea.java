package ObjetosList;

public class OTarea {

    public String name;
    public String description;
    public String fecha;
    public String dia;

    public OTarea() { }

    public OTarea(String name, String description, String fecha, String dia) {
        this.name = name;
        this.description = description;
        this.fecha = fecha;
        this.dia = dia;
    }
}
