package ObjetosList;

public class OUsuario {

    public String name;
    public String email;
    public String uid;
    public String rol;

    public OUsuario() {
    }

    public OUsuario(String name, String email, String userUid, String rol) {
        this.name = name;
        this.email = email;
        this.uid = userUid;
        this.rol = rol;
    }
}
