package ObjetosList;

public class OForo {

    public String titulo;
    public String descripcion;
    public String autor;
    public String fecha;

    public OForo() {}

    public OForo(String titulo, String descripcion, String autor, String fecha) {
        this.titulo = titulo;
        this.autor = autor;
        this.fecha = fecha;
        this.descripcion = descripcion;
    }
}
