package ObjetosList;

public class ONotificacion {

    public String titulo;
    public String descripcion;
    public String autor;
    public String fecha;

    public ONotificacion() {
    }

    public ONotificacion(String titulo, String descripcion, String autor, String fecha) {
        this.titulo = titulo;
        this.descripcion = descripcion;
        this.autor = autor;
        this.fecha = fecha;
    }
}
